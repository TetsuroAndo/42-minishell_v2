int	ft_ismetachr(char c)
{
	return (c == '|' || c == '&' || c == ';' || c == '<' || c == '>' || c == '('
		|| c == ')' || c == '$' || c == '`' || c == '"' || c == '\''
		|| c == '\\' || c == '*' || c == '?' || c == '[' || c == ']' || c == '{'
		|| c == '}');
}
