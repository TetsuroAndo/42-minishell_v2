#include "builtin_cmds.h"

/**
 * @brief カレントディレクトリのパスを表示する（pwdコマンド）
 * 
 * この関数は以下の処理を行います：
 * 1. シェル情報構造体からカレントディレクトリのパスを取得
 * 2. パスを標準出力に出力
 * 3. 改行を出力
 * 
 * @param shell シェル情報構造体
 * @return t_status 常にE_NONE（成功）を返す
 */
t_status	__pwd(t_shell *shell)
{
	printf("%s\n", shell->cwd);
	return (E_NONE);
}
